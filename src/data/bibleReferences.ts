// Funciones de utilidad
export function getPromiseOfTheDay(dayOfYear: number): BibleReference {
  const index = (dayOfYear - 1) % BIBLE_REFERENCES.length;
  return BIBLE_REFERENCES[index];
}

export function getRandomPromise(): BibleReference {
  const randomIndex = Math.floor(Math.random() * BIBLE_REFERENCES.length);
  return BIBLE_REFERENCES[randomIndex];
}

export function getPromiseByReference(reference: string): BibleReference | undefined {
  return BIBLE_REFERENCES.find(promise => promise.reference === reference);
}

export function getAllCategories(): string[] {
  const categories = new Set(BIBLE_REFERENCES.map(promise => promise.category));
  return Array.from(categories);
}

export function getPromisesByCategory(category: string): BibleReference[] {
  return BIBLE_REFERENCES.filter(promise => promise.category === category);
}
